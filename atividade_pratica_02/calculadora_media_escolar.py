# --- Calculadora de Média Escolar ---

# Notas do aluno
nota1 = 7.5
nota2 = 8.0
nota3 = 6.5

# Calcula a soma das notas
soma_notas = nota1 + nota2 + nota3

# Calcula a média dividindo a soma pelo número de notas
media = soma_notas / 3

# Exibe as notas e o resultado final da média
print(f"--- Calculadora de Média Escolar ---")
print(f"Nota 1: {nota1:.2f}")
print(f"Nota 2: {nota2:.2f}")
print(f"Nota 3: {nota3:.2f}")
print(f"Média Final: {media:.2f}") # Arredonda a média para duas casas decimais
print("-" * 30)